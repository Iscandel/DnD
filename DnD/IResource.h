#ifndef H__IRESOURCE__H
#define H__IRESOURCE__H

///////////////////////////////////////////////////////////////////////////////
/// Base class for resource
///////////////////////////////////////////////////////////////////////////////
class IResource
{
public:
	///////////////////////////////////////////////////////////////////////////
	/// Enum to describe the current status of the resource.
	///////////////////////////////////////////////////////////////////////////
	enum ResourceStatus
	{
		NOT_LOADED,
		IS_LOADING,
		IS_LOADED
	};
	
	///////////////////////////////////////////////////////////////////////////
	/// Constructor.
	///////////////////////////////////////////////////////////////////////////
	IResource(void);

	///////////////////////////////////////////////////////////////////////////
	/// Destructor.
	///////////////////////////////////////////////////////////////////////////
	~IResource(void);
	
	///////////////////////////////////////////////////////////////////////////
	/// Return the current resource status.
	///
	/// \return The status of the resources
	///////////////////////////////////////////////////////////////////////////
	virtual ResourceStatus getStatus() const {return myLoadingStatus;}

protected:
	ResourceStatus myLoadingStatus; ///< Current loading status.
};

#endif
